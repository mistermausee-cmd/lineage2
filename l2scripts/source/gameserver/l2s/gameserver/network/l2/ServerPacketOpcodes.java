	private final int _id;
	private final int _exId;

	private ServerPacketOpcodes()
	{
		int ordinal = ordinal();
		if(ordinal >= 0xFE)
		{
			_id = 0xFE;
			_exId = ordinal - 0xFE;
		}
		else
		{
			_id = ordinal;
			_exId = -1;
		}
	}

	public int getId()
	{
		return _id;
	}

	public int getExId()
	{
		return _exId;
	}
